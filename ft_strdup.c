#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*dup;
	size_t	size;

	size = ft_strlen(s) + 1;
	dup = malloc(size * sizeof(char));
	ft_memcpy(dup, s, size);
	return (dup);
}
