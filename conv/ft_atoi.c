#include "libft.h"

int	ft_atoi(const char *str)
{
	int		result;
	int		is_neg;
	size_t	i;

	result = 0;
	is_neg = 1;
	i = 0;
	while (ft_isspace(str[i]))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			is_neg = -1;
		i++;
	}
	while (ft_isdigit(str[i]))
	{
		result = result * 10 + (str[i] - '0');
		i++;
	}
	return (result * is_neg);
}
