#include "libft.h"

t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list	*map;
	t_list	*temp_next;

	map = NULL;
	while (lst)
	{
		ft_lstadd_back(&map, f(lst->content));
		temp_next = lst->next;
		ft_lstdelone(lst, del);
		lst = temp_next;
	}
	return (map);
}
