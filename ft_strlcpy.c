#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t size)
{
	size_t	i;

	i = 0;
	while (i < size - 1)
	{
		if (src[i] == 0)
			break ;
		dst[i] = src[i];
		i++;
	}
	if (i > 0)
		dst[i] = 0;
	return (ft_strlen(src));
}
